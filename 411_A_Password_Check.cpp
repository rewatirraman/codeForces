int main()
{
    string password;
    cin>>password;

    // the password length is at least 5 characters;
    if (password.length() < 5) {
        cout << "Too weak";
        return 0;
    }
    // the password contains at least one large English letter;
    // the password contains at least one small English letter;
    // the password contains at least one digit.
    // 0 --> 48 to 48+9
    // A --> 65 to 65+26
    // a --> 97 to 97+26
    bool upper=false, lower=false, num=false;
    for (int i = 0; i < password.length(); i++)
    {
        if (!lower) {
            if ( (password[i] >= 97) && (password[i] <= 123) ) {
                lower = true;
            }
        }

        if (!upper) {
            if ( (password[i] >= 65) && (password[i] <= 91) ) {
                upper = true;
            }            
        }

        if (!num) {
            if ( (password[i] >= 48) && (password[i] <= 57) ) {
                num = true;
            }            
        }

        if (lower && upper && num) {cout<<"Correct"; return 0;}
    }
    cout << "Too weak";
    return 0;
}