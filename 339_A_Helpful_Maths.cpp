int main() {
    int no_1=0, no_2=0, no_3=0;
    string question;
    cin >> question;

    for (int i = 0; i < question.length(); i++)
    {
        if (question[i] == '1') {no_1++;}
        else if (question[i] == '2') {no_2++;}
        else if (question[i] == '3') {no_3++;}
    }
    
    int j=0;
    while (no_1--)
    {
        question[j] = '1';
        j+=2;
    }
    while (no_2--)
    {
        question[j] = '2';
        j+=2;
    }    
    while (no_3--)
    {
        question[j] = '3';
        j+=2;
    }    

    cout << question;
    return 0;
}